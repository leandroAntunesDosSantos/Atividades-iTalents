class Calculadora {
    somar(num1, num2) {
        return Number(num1) + Number(num2);
    }

    subtrair(num1, num2) {
        return Number(num1) - Number(num2);
    }

    dividir(num1, num2) {
        return Number(num1) / Number(num2);
    }

    multiplicar(num1, num2) {
        return Number(num1) * Number(num2);
    }
} 